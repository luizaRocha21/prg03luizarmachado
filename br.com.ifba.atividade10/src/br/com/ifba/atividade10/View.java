    private void btnCriarPerfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarPerfilActionPerformed
        // Criação de perfil de usuário
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
        
        JTextField descricaoField = new JTextField(20);
        JTextField permissoesField = new JTextField(20);
        
        panel.add(new JLabel("Descrição:"));
        panel.add(descricaoField);
        panel.add(new JLabel("Permissões (separadas por vírgula):"));
        panel.add(permissoesField);
        
        int result = JOptionPane.showConfirmDialog(
            null, panel, "Criar Novo Perfil", 
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        
        if (result == JOptionPane.OK_OPTION) {
            Long id = (long) nextPerfilId++;
            String descricao = descricaoField.getText();
            PerfilUsuario perfil = new PerfilUsuario(id, descricao);
            
            // Adiciona permissões
            String[] permissoes = permissoesField.getText().split(",");
            for (String permissao : permissoes) {
                perfil.adicionarPermissao(permissao.trim());
            }
            
            perfis.add(perfil);
            mostrarConfirmacao("Perfil criado com sucesso!", 
                "ID: " + id + "\nDescrição: " + descricao + "\nPermissões: " + perfil.getPermissoes());
        }
    }//GEN-LAST:event_btnCriarPerfilActionPerformed

    private void btnCriarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarUsuarioActionPerformed
        // Criação de usuário
        if (perfis.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Crie perfis antes de criar usuários!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
        
        JTextField nomeField = new JTextField(20);
        JTextField emailField = new JTextField(20);
        JPasswordField senhaField = new JPasswordField(20);
        
        JComboBox<PerfilUsuario> perfilCombo = new JComboBox<>();
        for (PerfilUsuario perfil : perfis) {
            perfilCombo.addItem(perfil);
        }
        
        panel.add(new JLabel("Nome de Usuário:"));
        panel.add(nomeField);
        panel.add(new JLabel("Email:"));
        panel.add(emailField);
        panel.add(new JLabel("Senha:"));
        panel.add(senhaField);
        panel.add(new JLabel("Perfil:"));
        panel.add(perfilCombo);
        
        int result = JOptionPane.showConfirmDialog(
            null, panel, "Criar Novo Usuário", 
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        
        if (result == JOptionPane.OK_OPTION) {
            Long id = (long) nextUsuarioId++;
            String nome = nomeField.getText();
            String email = emailField.getText();
            String senha = new String(senhaField.getPassword());
            PerfilUsuario perfil = (PerfilUsuario) perfilCombo.getSelectedItem();
            
            Usuario usuario = new Usuario(id, nome, email, senha, perfil);
            usuarios.add(usuario);
            
            mostrarConfirmacao("Usuário criado com sucesso!", 
                "ID: " + id + "\nNome: " + nome + "\nEmail: " + email + "\nPerfil: " + perfil.getDescricao());
        }
    }//GEN-LAST:event_btnCriarUsuarioActionPerformed

    private void btnCriarSessaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarSessaoActionPerformed
        // Criação de sessão
        if (usuarios.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Crie usuários antes de criar sessões!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
        
        JComboBox<Usuario> usuarioCombo = new JComboBox<>();
        for (Usuario usuario : usuarios) {
            usuarioCombo.addItem(usuario);
        }
        
        panel.add(new JLabel("Usuário:"));
        panel.add(usuarioCombo);
        
        int result = JOptionPane.showConfirmDialog(
            null, panel, "Criar Nova Sessão", 
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        
        if (result == JOptionPane.OK_OPTION) {
            Long id = (long) nextSessaoId++;
            Usuario usuario = (Usuario) usuarioCombo.getSelectedItem();
            
            Sessao sessao = usuario.criarSessao();
            sessao.setId(id);
            sessoes.add(sessao);
            
            mostrarConfirmacao("Sessão criada com sucesso!", 
                "ID: " + id + "\nUsuário: " + usuario.getNomeUsuario() + 
                "\nToken: " + sessao.getToken());
        }
    }//GEN-LAST:event_btnCriarSessaoActionPerformed

    private void btnCriarAuditoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarAuditoriaActionPerformed
        // Criação de log de auditoria
        if (usuarios.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Crie usuários antes de criar logs de auditoria!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        JPanel panel = new JPanel();
        panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
        
        JTextField acaoField = new JTextField(20);
        
        JComboBox<Usuario> usuarioCombo = new JComboBox<>();
        for (Usuario usuario : usuarios) {
            usuarioCombo.addItem(usuario);
        }
        
        panel.add(new JLabel("Usuário:"));
        panel.add(usuarioCombo);
        panel.add(new JLabel("Ação:"));
        panel.add(acaoField);
        
        int result = JOptionPane.showConfirmDialog(
            null, panel, "Criar Log de Auditoria", 
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        
        if (result == JOptionPane.OK_OPTION) {
            Long id = (long) nextAuditoriaId++;
            Usuario usuario = (Usuario) usuarioCombo.getSelectedItem();
            String acao = acaoField.getText();
            String ip = obterIpLocal(); // IP obtido automaticamente
            
            LogAuditoria log = new LogAuditoria(id, usuario, acao, LocalDateTime.now(), ip);
            logs.add(log);
            
            mostrarConfirmacao("Log de auditoria criado com sucesso!", 
                "ID: " + id + "\nUsuário: " + usuario.getNomeUsuario() + 
                "\nAção: " + acao + "\nIP: " + ip + "\nData/Hora: " + log.getDataHora());
        }
    }//GEN-LAST:event_btnCriarAuditoriaActionPerformed

