    private void btnCuboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCuboActionPerformed
        // Abre diálogo para inserir a aresta
        // Cria um cubo com o valor informado
        // Exibe a área superficial e volume calculados
        Object[] fields = createInputFields("Aresta:");
        int option = JOptionPane.showConfirmDialog(this, fields, "Calcular Cubo", JOptionPane.OK_CANCEL_OPTION);
        
        if (option == JOptionPane.OK_OPTION) {
            try {
                double aresta = getValidatedDouble((JTextField)fields[1], "aresta");
                Cubo cubo = new Cubo(aresta);
                
                String resultado = String.format("Cubo:\nÁrea: %.2f\nVolume: %.2f", 
                    cubo.obterArea(), cubo.obterVolume());
                JOptionPane.showMessageDialog(this, resultado, "Resultado", JOptionPane.INFORMATION_MESSAGE);
            } catch (NumberFormatException ignored) {}
        }
    }//GEN-LAST:event_btnCuboActionPerformed

