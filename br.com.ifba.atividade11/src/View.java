    // Método para criar campos de entrada
    private Object[] createInputFields(String... labels) {
        Object[] components = new Object[labels.length * 2];
        for (int i = 0; i < labels.length; i++) {
            components[i*2] = labels[i];
            components[i*2+1] = new JTextField();
        }
        return components;
    }

    // Método para validar entrada numérica
    private double getValidatedDouble(JTextField field, String fieldName) {
        try {
            return Double.parseDouble(field.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, 
                "Valor inválido para " + fieldName + ". Digite um número válido.",
                "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
            throw e;
        }
    }

