    private void btnPiramideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPiramideActionPerformed
        // Abre diálogo para inserir lado da base, altura e altura triangular
        // Cria uma pirâmide com os valores informados
        // Exibe a área superficial e volume calculados
        Object[] fields = createInputFields("Lado da base:", "Altura:", "Altura triangular lateral:");
        int option = JOptionPane.showConfirmDialog(this, fields, "Calcular Pirâmide", JOptionPane.OK_CANCEL_OPTION);
        
        if (option == JOptionPane.OK_OPTION) {
            try {
                double base = getValidatedDouble((JTextField)fields[1], "lado da base");
                double altura = getValidatedDouble((JTextField)fields[3], "altura");
                double alturaTriangulo = getValidatedDouble((JTextField)fields[5], "altura triangular");
                Piramide piramide = new Piramide(base, altura, alturaTriangulo);
                
                String resultado = String.format("Pirâmide:\nÁrea: %.2f\nVolume: %.2f", 
                    piramide.obterArea(), piramide.obterVolume());
                JOptionPane.showMessageDialog(this, resultado, "Resultado", JOptionPane.INFORMATION_MESSAGE);
            } catch (NumberFormatException ignored) {}
        }
    }//GEN-LAST:event_btnPiramideActionPerformed

